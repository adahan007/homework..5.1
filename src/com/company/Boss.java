package com.company;

public class Boss {
    private int health;
    private int damage;
    private String shield;

    public int getHealth() {
        return health;
    }

    public int getDamage() {
        return damage;
    }

    public String getShield() {
        return shield;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public void setShield(String shield) {
        this.shield = shield;
    }

}
